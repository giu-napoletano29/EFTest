<template>
    <div>
        <Header :name="name">
            <div class="btn-toolbar mb-2 mb-md-0">
                <div class="btn-group me-2">
                    <button type="button" class="btn btn-sm btn-outline-primary" @click="OpenNewProduct()">Aggiungi prodotto</button>
                </div>
            </div>
        </Header>
        <Products
            :list="list"
            :listbrands="listbrands"
            :loaded="loaded"
            :error="error"
            :desc="desc"
            :orderbrand="orderbrand"
            :ordername="ordername"
            :orderprice="orderprice"
            @openmodal="OpenModal"
            @openDetail="OpenDetail"
            @brandfilter="BrandFilter"
            @orderbybrand="OrderByBrand"
            @orderbyname="OrderByName"
            @orderbyprice="OrderByPrice"
            @openedit="OpenEdit"
        >
            <Table
                :loaded="loaded"
                :error="error"
                :nrow="nrow"
            />
        </Products>

        <Pagination
            :totalPages="totalpages"
            :maxVisibleButtons="maxVisibleButtons"
            :page="page"
            @pageChanged="pageChange"
        />
        <DeleteModal
            :open="open"
            @closemodal="CloseModal"
            @delete="Delete"
        />     
        <RedirectModal
            :success="successModalOpen"
            :OpError="OpError"
            @closemodal="CloseModal"
        />        
    </div>
</template>

<script>
    import MainPagesUtils from '@/utilities/MainPagesUtils.js' 
    import BrandsProductsUtils from '@/utilities/BrandsProductsUtils.js'
    import Header from '@/components/Header.vue'
    import Products from '@/components/Products.vue'
    import Table from '@/components/Table.vue'
    import Pagination from '@/components/Pagination.vue'
    import DeleteModal from '@/components/DeleteModal.vue'
    import RedirectModal from '@/components/RedirectModal.vue'
    import {Factory} from './../wrappers/Factory'
    const ProductsRepo = Factory.get('products')
    const BrandsRepo = Factory.get('brands')

    export default {
        mixins: [MainPagesUtils, BrandsProductsUtils],
        
        components: {
            Products,
            Header,
            Pagination,
            DeleteModal,
            Table,
            RedirectModal
        },

        data(){
            return{
                name: 'Prodotti',
                listbrands: [],
                nrow: 7,
                orderbrand: false,
                ordername: false,
                orderprice: false,
                desc: false,
            }
        },

        methods: {
            async loadElements(){
                this.loaded = false
                const {data} = await ProductsRepo.getallpagedsized(this.pageSize, this.page, this.params)
                this.loaded = true
                this.list = data;
            },
            async loadBrands(){
                const {data} = await BrandsRepo.get()
                this.listbrands = data;
            },

            Delete(){
                const data = ProductsRepo.delete(this.idEl)
                this.open = false
                this.DeleteSpecComponent(data)
            },

            OpenNewProduct(){
                this.$router.push('/products/new')
            },

            OpenDetail(val){
                this.$router.push('/products/'+val)
            },

            OpenEdit(id){
                this.$router.push('/products/edit/' + id)
            },

            BrandFilter(filter){
                if(/^\d+$/.test(filter)){
                    this.params.brand = filter
                }else{
                    delete this.params.brand
                }
                this.loadElements();
            },

            ResetOrder(){
                this.orderbrand = false
                this.ordername = false
                this.orderprice = false
                delete this.params.orderbyBrand
                delete this.params.orderbyName
                delete this.params.orderbyPrice
            },

            OrderByBrand(){
                this.ResetOrder()
                this.orderbrand = true
                this.params.orderbyBrand = this.orderbrand
                this.OrderByDesc()
            },
            OrderByName(){
                this.ResetOrder()
                this.ordername = true
                this.params.orderbyName = this.ordername
                this.OrderByDesc()
            },
            OrderByPrice(){
                this.ResetOrder()
                this.orderprice = true
                this.params.orderbyPrice = this.orderprice
                this.OrderByDesc()
            },
            OrderByDesc(){
                this.desc = !this.desc
                this.params.desc = this.desc
                this.loadElements();
            },
        },

        created() {
            this.loadElements();
            this.loadBrands();
        },

        mounted(){
            if(this.$route.name==='ProductsSuccess'){
                this.successModalOpen = true
            }else if(this.$route.name==='ProductsError'){
                this.successModalOpen = true
                this.OpError = true
            }
        }
    }
</script>