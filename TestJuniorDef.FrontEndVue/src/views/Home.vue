<template>
    <div id="app">
        <!--<img alt="Vue logo" src="../assets/logo.png">-->
        <b-container>
            <Header />
            <Home :test="prova"
                  :axi="brand"
                  :inc="numInc"
                  :dec="numDec"
                  :bset="bset"
                  @val="bset" />
        </b-container>
    </div>
</template>

<script>
    import Home from '@/components/Home.vue'
    import Header from '@/components/Header.vue'
    import axios from 'axios';

    export default {
        name: 'App',
        components: {
            Home,
            Header
        },

        data() {
            return {
                prova: 34,
                brand: ''
            }
        },

        created() {
            try {
                axios.get('https://localhost:5001/Brand/6').then(response => (this.brand = response.data))
            } catch (e) {
                console.error(e)
            }
        },

        methods: {
            numInc() {
                this.prova++
            },
            numDec() {
                this.prova--
            },
            bset(val) {
                this.prova = val
            }
        }
    }
</script>

